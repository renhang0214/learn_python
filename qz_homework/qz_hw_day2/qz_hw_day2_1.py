"""
有如下值集合 [11,22,33,44,55,66,77,88,99,90...]，将所有大于 66 的值保存至字典的第一个key中，将小于 66 的值保存至第二个key的值中。
即： {'k1': 大于66的所有值, 'k2': 小于66的所有值}
"""

l1 = [11, 22, 33, 44, 55, 66, 77, 88, 99, 90]

# 定义一个空字典
dic = {
    "k1": [],
    "k2": []
}
# 开始循环
for i in l1:
    if i > 66:  # 判断元素大于66
        dic["k1"].append(i)  # 增加到字典中的k1内
    elif i < 66:  # 小于66
        dic["k2"].append(i)  # 增加到字典中的k2内
print(dic)  # 输出字典
